function styleText(text, style = 'normal') {
    if (!text) return '';
    if (!fonts[style]) return text;

    const normalChars = fonts.normal;
    const styledChars = fonts[style];
    
    return text.split('').map(char => {
        const index = normalChars.indexOf(char);
        return index !== -1 ? styledChars[index] || char : char;
    }).join('');
}

module.exports = { styleText };